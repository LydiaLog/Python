first = int(input("정수 입력 : "))
cal = str(input("연산자 입력 : " ))
second = int(input("정수 입력 : "))

if cal == '+' :
     print(first, cal, second, '=', first + second)
    
elif cal == '-' :
     print(first, cal, second, '=', first - second)
        
elif cal == '*' :
     print(first, cal, second, '=', first * second)
        


    